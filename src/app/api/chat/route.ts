function getResponse(userMessage: string): string {
  const lower = userMessage.toLowerCase();
  for (const rule of RULES) {
    if (rule.keywords.some((kw) => lower.includes(kw))) {
      return rule.response;
    }
  }
  return FALLBACK;
}

// Simulate streaming: yield the response text in small chunks
async function* streamResponse(text: string): AsyncGenerator<string> {
  const words = text.split(/(?<= )/); // split keeping trailing spaces
  for (const word of words) {
    yield word;
    // Small random-ish delay between words to feel natural
    await new Promise((r) => setTimeout(r, 18 + (word.length % 3) * 8));
  }
}

export async function POST(req: NextRequest) {
  const { messages } = await req.json();
  const lastMessage: { role: string; content: string } =
    messages[messages.length - 1];
  const userText = lastMessage?.content ?? "";

  const response = getResponse(userText);

  const encoder = new TextEncoder();
  const stream = new ReadableStream({
    async start(controller) {
      for await (const chunk of streamResponse(response)) {
        controller.enqueue(encoder.encode(chunk));
      }
      controller.close();
    },
  });

  return new Response(stream, {
    headers: {
      "Content-Type": "text/plain; charset=utf-8",
      "Cache-Control": "no-cache",
      "X-Accel-Buffering": "no",
    },
  });
}
